const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(scriptDirectory, "..");
const packageMetadata = JSON.parse(
  await readFile(
    path.join(webRoot, "node_modules", "simple-icons", "package.json"),
    "utf8",
  ),
);
if (packageMetadata.version !== SIMPLE_ICONS_VERSION) {
  throw new Error(
    `Expected Simple Icons ${SIMPLE_ICONS_VERSION}, received ${packageMetadata.version}`,
  );
}
const outputDirectory = path.join(
  webRoot,
  "public",
  "technology-logos",
  "simple-icons",
);
const generatedModule = path.join(
  webRoot,
  "src",
  "features",
  "market",
  "technology-icon-assets.generated.ts",
);

await rm(outputDirectory, { force: true, recursive: true });
await mkdir(outputDirectory, { recursive: true });

const assets = [];
for (const exportName of EXPORT_NAMES) {
  const icon = simpleIcons[exportName];
  if (!icon || typeof icon !== "object" || !("slug" in icon)) {
    throw new Error(`Missing Simple Icons export: ${exportName}`);
  }
  const source = path.join(
    webRoot,
    "node_modules",
    "simple-icons",
    "icons",
    `${icon.slug}.svg`,
  );
  const destination = path.join(outputDirectory, `${icon.slug}.svg`);
  const originalSvg = await readFile(source, "utf8");
  const coloredSvg = originalSvg.replace(
    "<svg ",
    `<svg fill="#${icon.hex.toUpperCase()}" `,
  );
  if (coloredSvg === originalSvg) {
    throw new Error(`Unable to apply brand color: ${exportName}`);
  }
  await writeFile(destination, coloredSvg);
  assets.push({
    key: icon.slug,
    src: `/technology-logos/simple-icons/${icon.slug}.svg`,
  });
}

assets.sort((left, right) => left.key.localeCompare(right.key));
const serializedAssets = JSON.stringify(
  Object.fromEntries(assets.map((asset) => [asset.key, asset])),
  null,
  2,
);
const source = [
  "/* Generated by scripts/sync-technology-icons.mjs. */",
  `export const GENERATED_TECHNOLOGY_BRAND_ASSETS = ${serializedAssets} as const;`,
  "export type GeneratedTechnologyBrandKey = keyof typeof GENERATED_TECHNOLOGY_BRAND_ASSETS;",
  "",
].join("\n");
await writeFile(generatedModule, source);
